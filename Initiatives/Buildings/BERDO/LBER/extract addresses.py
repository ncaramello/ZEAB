# Heuristic: "Is this token a leading house-number candidate?"
def is_house_number(token):
    # Matches "123" or "123A" or "120B" etc. (digits followed by optional letters)
    return bool(re.match(r'^\d+[A-Za-z]*$', token))

# Heuristic: "Does this token look like extra info rather than part of the street name?"
# e.g. purely numeric suite/floor, tokens starting with B/G/P + digits, & references, "OFFICE", "BMT", "PH", etc.
def is_additional_info(token):
    token_upper = token.upper()
    # if it has an ampersand anywhere, treat as extra
    if '&' in token_upper:
        return True
    # if it has a dash in it e.g. "1-2"
    if '-' in token.upper():
        return True
    # purely numeric (like "202")
    if re.match(r'^\d+$', token_upper):
        return True
    # letter + digits (like "B101", "P109", "G7", "1B") 
    if re.match(r'^(?=.*[0-9])(?=.*[a-zA-Z])[a-zA-Z0-9]+$', token_upper):
        return True
    # some known words we want to exclude if they appear
    if token_upper in ["BMT","OFFICE","PH","PK","NC10"]:
        return True
    return False

unique_addresses = []
seen = set()

for line in raw_addresses:
    line = line.strip()
    if not line:
        continue
    tokens = line.split()
    # 1) Possibly remove second house number
    house_num = []
    idx = 0
    # Grab up to one house number
    if idx < len(tokens) and is_house_number(tokens[idx]):
        house_num.append(tokens[idx])
        idx += 1
    # If there's another house number immediately after, skip it
    if idx < len(tokens) and is_house_number(tokens[idx]):
        # skip this one entirely
        idx += 1
    
    # Now collect the rest as street name until we hit extra info
    street_tokens = []
    while idx < len(tokens):
        if is_additional_info(tokens[idx]):
            break
        street_tokens.append(tokens[idx])
        idx += 1
    
    cleaned = " ".join(house_num + street_tokens)
    # Make sure we don't include blank lines or empty results
    if cleaned and cleaned not in seen:
        seen.add(cleaned)
        unique_addresses.append(cleaned)

# Show the final unique addresses:
for addr in unique_addresses:
    print(addr)